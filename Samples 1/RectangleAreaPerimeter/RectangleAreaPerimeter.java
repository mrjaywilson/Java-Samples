/*
 * Displays the area and perimeter of a rectangle
 * that has a width of 4.5 and a height of 7.9
 * 
 * @author Jay Wilson
 */

public class RectangleAreaPerimeter {

	public static void main(String[] args) {
		
		// Calculate perimeter and area of 4.5 X 7.9 rectangle
		double perimeter = 2 * (4.5 * 7.9);
		double area = 4.5 * 7.9;

		// Output calculation to console
		System.out.println("Perimeter: " + perimeter);
		System.out.println("Area: " + area);
	}
}
